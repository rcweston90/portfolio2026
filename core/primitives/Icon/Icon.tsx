/**
 * Icon Component
 * 
 * A centralized icon component that provides consistent icons throughout the app.
 * All icons are rendered as inline SVGs for easy styling and customization.
 * 
 * @example
 * ```tsx
 * // Basic usage
 * <Icon name="arrow-right" />
 * 
 * // With size
 * <Icon name="github" size="lg" />
 * 
 * // With custom styling
 * <Icon name="mail" className="text-accent-primary" />
 * 
 * // With accessibility label
 * <Icon name="close" aria-label="Close menu" />
 * ```
 */
export function Icon({ 
  name, 
  size = 'md', 
  className = '', 
  'aria-label': ariaLabel,
  ...props 
}: IconProps) {
  const IconComponent = icons[name];
  const pixelSize = sizeMap[size];

  if (!IconComponent) {
    console.warn(`Icon "${name}" not found`);
    return null;
  }

  return (
    <IconComponent
      width={pixelSize}
      height={pixelSize}
      className={className}
      aria-hidden={ariaLabel ? undefined : true}
      aria-label={ariaLabel}
      role={ariaLabel ? 'img' : undefined}
      {...props}
    />
  );
}

export default Icon;

